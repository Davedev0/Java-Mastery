
// Basic array index example
public class arraysIndex {
    public static void main(String[] args) {
        String userNames[] = {
            "daveDev", "daveFuentes01",
            "jhondave0"
        };
            
        String emails[] = {
            "dave9@gmail.com",
            "jhondave@gmail.com",
            "pogi@gmail.com"
        };
        
        String passwords[] = {
            "123pogi", "palatuti0123",
            "matt123"
        };
        
        int i = 2;
        System.out.println("Username: " + userNames[i]);
        System.out.println("Email: " + emails[i]);
        System.out.print("Password: " + passwords[i]);
    }
    
}
